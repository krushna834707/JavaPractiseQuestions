package PractiseQue;

public class ReverseRightHalfPyramid {
    public static void main(String[] args) {

        //PractiseQue.ReverseRightHalfPyramid
        for(int i=5;i>=0;i--){
            for(int j=i;j>=0;j--){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
